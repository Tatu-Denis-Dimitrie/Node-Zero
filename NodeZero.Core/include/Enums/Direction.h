#pragma once

/// <summary>
/// Direcții cardinale pentru mișcare
/// </summary>
enum class Direction
{
    /// <summary>
    /// Direcție în sus
    /// </summary>
    Up,

    /// <summary>
    /// Direcție în jos
    /// </summary>
    Down,

    /// <summary>
    /// Direcție la stânga
    /// </summary>
    Left,

    /// <summary>
    /// Direcție la dreapta
    /// </summary>
    Right,

    /// <summary>
    /// Fără direcție (stationar)
    /// </summary>
    None
};
